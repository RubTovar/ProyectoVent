     public void Llenado(){
        String columnas[] ={"Clave","Nombre","Telefono","Pais","Direccion","Correo","Horario","Rfc","Actualizar","Eliminar"};
        List lista;
        int fila=-1;
        try {
            Object[][] data =new Object[fc.ListasTienda(dt).size()][11];
            lista = fc.ListasTienda(dt);
            Iterator it= lista.iterator();
            while (it.hasNext()) {
                dt =(tiendaDT) it.next();
                fila++;
                data[fila][0]= dt.getIdtienda();
                data[fila][1]= dt.getNombre();
                data[fila][2]= dt.getTelefono();
                data[fila][3]= dt.getPais();
                data[fila][4]= dt.getDireccion();
                data[fila][5]= dt.getCorreo();
                data[fila][6]= dt.getHorario();
                data[fila][7]= dt.getRfc();
                data[fila][8]= "Actualiza";
                data[fila][9]= "Eliminar";
                tabla.setModel(new javax.swing.table.DefaultTableModel(data, columnas));
               
        }} catch (Exception e) {
        }
    }

