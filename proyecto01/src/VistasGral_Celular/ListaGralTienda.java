    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        // TODO add your handling code here:
        dt.setIdtienda(Integer.parseInt(tabla.getValueAt(tabla.getSelectedRow(), 0).toString()));
        dt.setNombre(tabla.getValueAt(tabla.getSelectedRow(), 1).toString());
        dt.setTelefono(Integer.parseInt(tabla.getValueAt(tabla.getSelectedRow(), 2).toString()));
        dt.setPais(tabla.getValueAt(tabla.getSelectedRow(), 3).toString());
        dt.setDireccion(tabla.getValueAt(tabla.getSelectedRow(), 4).toString());
        dt.setCorreo(tabla.getValueAt(tabla.getSelectedRow(), 5).toString());
        dt.setHorario(tabla.getValueAt(tabla.getSelectedRow(), 6).toString());
        dt.setRfc(tabla.getValueAt(tabla.getSelectedRow(), 7).toString());
       
        
        //Actualizar
        if (tabla.getSelectedColumn()==8) {
            if (JOptionPane.showConfirmDialog(this, "Deseas actualizar?","Actualizar",JOptionPane.YES_NO_OPTION)== JOptionPane.YES_OPTION) {
                try {
                    fc.cambiaTienda(dt);
                } catch (Exception e) {
                }
                
            }
        }
        //Eliminar
        if (tabla.getSelectedColumn()==9) {
            if (JOptionPane.showConfirmDialog(this, "Deseas eliminar?","Eliminar",JOptionPane.YES_NO_OPTION)== JOptionPane.YES_OPTION) {
                try {
                    fc.borraTienda(dt);
                    //Llenado();
                } catch (Exception e) {
                }
                
            }
        }
    }//GEN-LAST:event_tablaMouseClicked


