     public void Llenado(){
        String columnas[] ={"Clave","Nombre","A.Paterno","A.Materno","Rfc","Celular","FechaNacimiento","Correo","Sexo","Actualizar","Eliminar"};
        List lista;
        int fila=-1;
        try {
            Object[][] data =new Object[fc.seleccionarrtodoCLIENTE(dt).size()][13];
            lista = fc.seleccionarrtodoCLIENTE(dt);
            Iterator it= lista.iterator();
            while (it.hasNext()) {
                dt =(clienteDT) it.next();
                fila++;
                data[fila][0]= dt.getIdcliente();
                data[fila][1]= dt.getNombre();
                data[fila][2]= dt.getApaterno();
                data[fila][3]= dt.getAmaterno();
                data[fila][4]= dt.getRfc();
                data[fila][5]= dt.getCelular();
                data[fila][6]= dt.getFechanacimiento();
                data[fila][7]= dt.getCorreo();
                data[fila][8]= dt.getSexo();
                data[fila][9]= "Actualiza";
                data[fila][10]= "Eliminar";
                tabla.setModel(new javax.swing.table.DefaultTableModel(data, columnas));
               
        }} catch (Exception e) {
        }
    }
    

