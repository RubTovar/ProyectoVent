    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        // TODO add your handling code here:
        dt.setIdcliente(Integer.parseInt(tabla.getValueAt(tabla.getSelectedRow(), 0).toString()));
        dt.setNombre(tabla.getValueAt(tabla.getSelectedRow(), 1).toString());
        dt.setApaterno(tabla.getValueAt(tabla.getSelectedRow(), 2).toString());
        dt.setAmaterno(tabla.getValueAt(tabla.getSelectedRow(), 3).toString());
        dt.setRfc(tabla.getValueAt(tabla.getSelectedRow(), 4).toString());
        dt.setCelular(Integer.parseInt(tabla.getValueAt(tabla.getSelectedRow(), 5).toString()));
        dt.setFechanacimiento(Date.valueOf(tabla.getValueAt(tabla.getSelectedRow(), 6).toString()));
        dt.setCorreo(tabla.getValueAt(tabla.getSelectedRow(), 7).toString());
        dt.setSexo(tabla.getValueAt(tabla.getSelectedRow(), 8).toString());
        
        //Actualizar
        if (tabla.getSelectedColumn()==9) {
            if (JOptionPane.showConfirmDialog(this, "Deseas actualizar?","Actualizar",JOptionPane.YES_NO_OPTION)== JOptionPane.YES_OPTION) {
                try {
                    fc.actualizarrCLIENTE(dt);
                } catch (Exception e) {
                }
                
            }
        }
        //Eliminar
        if (tabla.getSelectedColumn()==10) {
            if (JOptionPane.showConfirmDialog(this, "Deseas eliminar?","Eliminar",JOptionPane.YES_NO_OPTION)== JOptionPane.YES_OPTION) {
                try {
                    fc.eliminarrCLIENTE(dt);
                    //Llenado();
                } catch (Exception e) {
                }
                
            }
        }
    }//GEN-LAST:event_tablaMouseClicked


