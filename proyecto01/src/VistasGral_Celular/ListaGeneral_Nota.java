    private void TablonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablonMouseClicked
        // TODO add your handling code here:
        dt.setIdnota(Integer.parseInt(Tablon.getValueAt(Tablon.getSelectedRow(), 0).toString()));
        dt.setIdcelular(Integer.parseInt(Tablon.getValueAt(Tablon.getSelectedRow(), 1).toString()));
        dt.setFechapago(Date.valueOf(Tablon.getValueAt(Tablon.getSelectedRow(), 2).toString()));
        dt.setDetalleventa(Tablon.getValueAt(Tablon.getSelectedRow(), 3).toString());
        dt.setCantidadproductos(Integer.parseInt(Tablon.getValueAt(Tablon.getSelectedRow(), 4).toString()));
        dt.setPreciototal(Double.parseDouble(Tablon.getValueAt(Tablon.getSelectedRow(), 5).toString()));   
        
        //Actualizar
        if (Tablon.getSelectedColumn()==6) {
            if (JOptionPane.showConfirmDialog(this, "Deseas actualizar?","Actualizar",JOptionPane.YES_NO_OPTION)== JOptionPane.YES_OPTION) {
                try {
                    fc.actualizarrNOTA(dt);
                } catch (Exception e) {
                }
                
            }
        }
        //Eliminar
        if (Tablon.getSelectedColumn()==7) {
            if (JOptionPane.showConfirmDialog(this, "Deseas eliminar?","Eliminar",JOptionPane.YES_NO_OPTION)== JOptionPane.YES_OPTION) {
                try {
                    fc.eliminarrNOTA(dt);
                    //Llenado();
                } catch (Exception e) {
                }
                
            }
        }
    }//GEN-LAST:event_TablonMouseClicked


