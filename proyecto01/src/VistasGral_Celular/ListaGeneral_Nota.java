    public void Llenado(){
        String columnas[] ={"ID Nota","ID Celular","Fecha del pago","Detalle de la venta","Cantidad de Productos","Precio Total","Actualiza","elimina"};
        List lista;
        int fila=-1;
        try {
            Object[][] data =new Object[fc.seleccionarrtodoNOTA(dt).size()][9];
            lista = fc.seleccionarrtodoNOTA(dt);
            Iterator it= lista.iterator();
            while (it.hasNext()) {
                dt =(notaDT) it.next();
                fila++;
                data[fila][0]= dt.getIdnota();
                data[fila][1]= dt.getIdcelular();
                data[fila][2]= dt.getFechapago();
                data[fila][3]= dt.getDetalleventa();
                data[fila][4]= dt.getCantidadproductos();
                data[fila][5]= dt.getPreciototal();
                data[fila][6]= "Actualiza";
                data[fila][7]= "Eliminar";
                Tablon.setModel(new javax.swing.table.DefaultTableModel(data, columnas));
               
        }} catch (Exception e) {
        }
    }

